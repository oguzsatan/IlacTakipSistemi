    private void btn_ilac_silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ilac_silActionPerformed
        // TODO add your handling code here:
        getList();
        if (tbl_kullanılan_ilaclar.getSelectedRow() != -1) {

            int selectedRowIndex = tbl_kullanılan_ilaclar.getSelectedRow();
            dtm.removeRow(selectedRowIndex);
            try {
                Connection con = DriverManager.getConnection("jdbc:derby://localhost:1527/sample", "app", "app");
                String sqlUpdate = "DELETE FROM ILAC WHERE ID=(?)";
                PreparedStatement ps = con.prepareStatement(sqlUpdate);
                ps.setInt(1, ilaclar.get(selectedRowIndex).getId());
                ps.executeUpdate();
                con.commit();
                con.close();
                
                
                File file = new File("ilaclar.txt");
                if (!file.exists()) {
                    Files.createFile(file.toPath());
                }

       
                FileWriter fw = new FileWriter(file.toString());
                BufferedWriter bw = new BufferedWriter(fw);
                
                getList();
                
                for (Ilac i : ilaclar) {
                    bw.write(i.getId() + "-" + i.getAd() + "-" + i.getTur() + "\n");
                }
               
                bw.close();

            }catch(IOException e){
                
            } catch (SQLException ex) {
                System.out.println(ex.getMessage());
                ex.printStackTrace();
                Logger.getLogger(ilac_takip_anasayfa.class.getName()).log(Level.SEVERE, null, ex);
            }
          

        }

    }//GEN-LAST:event_btn_ilac_silActionPerformed

    private void btn_ilac_araActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ilac_araActionPerformed
        // TODO add your handling code here:
        getList();

        try {
            DefaultListModel dlm = new DefaultListModel();
            lst_arama_sonuc.removeAll();
            lst_arama_sonuc.setModel(dlm);
            
            Path p = Paths.get("ilaclar.txt");
            FileReader fr = new FileReader(p.toFile());
            String line;
            BufferedReader br = new BufferedReader(fr);
            while ((line = br.readLine()) != null) {
                String[] arr = line.split("-");
                if (cb_arama.getSelectedIndex() == 0) {
                    if (Integer.parseInt(arr[0]) == Integer.parseInt(txt_ilac_ara.getText())) {
                        String tur = "";
                        for (int j = 0; j < turler.size(); j++) {
                            if (Integer.valueOf(arr[2]) == turler.get(j).getTurid()) {
                                tur = turler.get(j).getAd();
                            }
                        }
                        dlm.addElement(arr[0] + " " + arr[1] + " " + tur); 

                    }
                    lst_arama_sonuc.setModel(dlm);
                } else {
                   if (arr[1].equals(txt_ilac_ara.getText())) {
                        String tur = "";
                        for (int j = 0; j < turler.size(); j++) {
                            if (Integer.valueOf(arr[2]) == turler.get(j).getTurid()) {
                                tur = turler.get(j).getAd();
                            }
                        }
                        dlm.addElement(arr[0] + " " + arr[1] + " " + tur); 

                    }
                    lst_arama_sonuc.setModel(dlm);
                }
            }
            br.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "gecersiz deger girdiniz!", "Hata", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btn_ilac_araActionPerformed

