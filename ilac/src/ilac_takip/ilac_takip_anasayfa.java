    private void btn_ilac_degistirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ilac_degistirActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tbl_kullanılan_ilaclar.getSelectedRow();

        if (tbl_kullanılan_ilaclar.getSelectedRow() != -1) {
            try {
                dtm.insertRow(selectedRowIndex, new Object[]{dtm.getValueAt(selectedRowIndex, 0), dtm.getValueAt(selectedRowIndex, 1),
                    dtm.getValueAt(selectedRowIndex, 2), dtm.getValueAt(selectedRowIndex, 3),
                    dtm.getValueAt(selectedRowIndex, 4), dtm.getValueAt(selectedRowIndex, 5), dtm.getValueAt(selectedRowIndex, 6)});

                dtm.removeRow(selectedRowIndex + 1);

                Connection con = DriverManager.getConnection("jdbc:derby://localhost:1527/sample", "app", "app");
                String sqlUpdate = "UPDATE ILAC SET AD = (?),TUR=(?),KS=(?),DOZ=(?),SKT=(?),KALANGUN=(?) WHERE ID=(?) ";
                PreparedStatement ps = con.prepareStatement(sqlUpdate);
                
                ps.setString(1, dtm.getValueAt(selectedRowIndex, 1).toString());
                int tur=0;
                for (int i = 0; i < turler.size(); i++) {
                    if (dtm.getValueAt(selectedRowIndex, 2).toString().equals(turler.get(i).getAd())) {
                        tur=turler.get(i).getTurid();
                    }
                }
                ps.setInt(2,tur);
                ps.setInt(3, Integer.valueOf(dtm.getValueAt(selectedRowIndex, 3).toString()));
                ps.setInt(4, Integer.valueOf(dtm.getValueAt(selectedRowIndex, 4).toString()));
                ps.setString(5, dtm.getValueAt(selectedRowIndex, 5).toString());
                ps.setInt(6, Integer.valueOf(dtm.getValueAt(selectedRowIndex, 6).toString()));
                ps.setInt(7, Integer.valueOf(dtm.getValueAt(selectedRowIndex, 0).toString()));

                ps.executeUpdate();
                con.commit();
                con.close();
                
                getList();

            } catch (ClassCastException e) {
                JOptionPane.showMessageDialog(this, "lütfen parametreleri kontrol ediniz (ilaç  no değiştirilemez)");

            } catch (NumberFormatException n) {
                JOptionPane.showMessageDialog(this, "lütfen parametreleri kontrol ediniz");
            } catch (SQLException ex) {
                Logger.getLogger(ilac_takip_anasayfa.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_btn_ilac_degistirActionPerformed

    private void sonrakiGunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sonrakiGunActionPerformed
        // TODO add your handling code here:

        for (int i = 0; i < ilaclar.size(); i++) {
            if (ilaclar.get(i).getKalangun() > 0) {
                try {
                    Connection con = DriverManager.getConnection("jdbc:derby://localhost:1527/sample", "app", "app");
                    String sqlUpdate = "UPDATE ILAC SET KALANGUN=(?) WHERE ID=(?)";
                    PreparedStatement ps = con.prepareStatement(sqlUpdate);
                    ps.setInt(1, ilaclar.get(i).getKalangun() - 1);
                    ps.setInt(2, ilaclar.get(i).getId());
                    ps.executeUpdate();
                    con.commit();
                    con.close();
                    dtm.setValueAt(ilaclar.get(i).getKalangun() - 1, i, 6);
                    ilaclar.get(i).setKalangun(ilaclar.get(i).getKalangun() - 1);
                } catch (SQLException ex) {
                    System.out.println(ex.getMessage());
                }

            }

        }


    }//GEN-LAST:event_sonrakiGunActionPerformed

