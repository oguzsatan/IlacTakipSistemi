    void getList() {

        EntityManagerFactory emf = Persistence.createEntityManagerFactory("ilac_takip_denemePU");
        EntityManager em = emf.createEntityManager();
        Query q = em.createQuery("select i from Ilac i"); //"jdbc:derby://localhost:1527/sample [app on APP]"
        ilaclar = q.getResultList();
        q = em.createQuery("SELECT t FROM Tur t");
        turler = q.getResultList();
    }

